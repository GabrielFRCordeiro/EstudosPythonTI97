"""
Operadores Matematicos
+ soma
- subtracao
* multiplicacao
/ divisao real
// divisao inteiro
** potencia
% modulo
"""

# Formatar a saida de dados
print(f"Soma: {5 + 2}")
print(f"Subtração: {5 - 2}")
print(f"Multiplicação: {5 * 2}")
print(f"Divisão real: {5 / 2}")
print(f"Divisão inteira: {5 // 2}")
print(f"Potência: {5 ** 2}")
print(f"Módulo: {5 % 2}")
